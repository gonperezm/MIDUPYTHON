#operadores de comparacion: devuelven un booleano

print("5 < 3:", 5>3) #True
print("5 > 3:", 5<3) #False
print("5 == 5:", 5==5) #True (igualdad)
print("5 != 3:", 5 !=3) #True ( desigualdad)
print("5 >= 5:", 5>=5) #True (mayor o igual que)
print("5 <= 3:", 5>=3) #False (menor o igual que)
